uint8_t buff[384];//48*8

void SendLine(uint8_t* data);
void Conv(uint8_t* data);

int main(void) {
	
	TCCR0A = 0;
	TCCR0B = 3;//1/64
	TIMSK0 = 0;

	TCCR1A = 0;
	TCCR1B = 5;//1/1024 (16000000/1024=15625)tick/s
	TCCR1C = 0;
	TIMSK1 = 0;
		
	DDR_595_6 = 0xFC;//B11111100;
	DDR_CLK_ON;
	DDR_OE_ON;
	DDR_PNP1_ON;
	DDR_LED1_ON;
	DDR_LED2_ON;
	
	while(true)
	{
		//init/////////////////////////////////////////////////////////////////////////////////////////////////////////////
		TCNT1 = 0;//0.000064 s/tick
		for(uint16_t i=0;i<48;i++)
		{
			for(uint8_t t = 0;t<6;t++)
			{
				buff[i*8+t] = pgm_read_byte_near(data+i*6+t);
			}
			Conv(buff+i*8);
		}
		//part1///////////////////////////////////////////////////////////////////////////////////////////////////////////
		while(TCNT1<100);//0.0064s
		//flash led
		TCNT0 = 0;
		while(TCNT0<1);
		PORT_LED1_ON;//PNP on H
		while(TCNT0<20);
		PORT_LED1_OFF;//PNP disable L

		
		while(TCNT1<200);//0.0064s
		for(uint16_t i=0;i<48;i++)
		{
			TCNT0 = 0;
			SendLine(buff+i*8);
			while(TCNT0<187);
			PORT_PNP1_ON;//PNP on H
			PORT_OE_OFF;//OE on L
			while(TCNT0<235);
			PORT_PNP1_OFF;//PNP disable L
			PORT_OE_ON;//OE disable H
		}
		
		while(TCNT1<7812);//part2///////////////////////////////////////////////////////////////////////////////////////////

		while(TCNT1<7812+100);//0.0064s
		//flash led
		TCNT0 = 0;
		while(TCNT0<1);
		PORT_LED1_ON;//PNP on H
		while(TCNT0<20);
		PORT_LED1_OFF;//PNP disable L

		while(TCNT1<7812+200);//0.0064s
		for(uint16_t i=0;i<48;i++)
		{
			TCNT0 = 0;
			SendLine(buff+47-i*8);
			while(TCNT0<187);
			PORT_PNP1_ON;//PNP on H
			PORT_OE_OFF;//OE on L
			while(TCNT0<235);
			PORT_PNP1_OFF;//PNP disable L
			PORT_OE_ON;//OE disable H
		}
		
		while(TCNT1<15625);
    }
}

void SendLine(uint8_t* data)
{
	PORT_CLK_OFF;
	uint8_t* idx = data + 8;
	for(uint8_t i=0;i<8;i++)
	{
		idx--;
		PORT_595_6 = *idx;//setData bit0.1 is usart use bit2~7
		PORT_CLK_ON; //shift clock up
		PORT_CLK_OFF; //shift clock down
	}
	PORT_CLK_ON; //shift clock up
	PORT_CLK_OFF; //shift clock down
}

